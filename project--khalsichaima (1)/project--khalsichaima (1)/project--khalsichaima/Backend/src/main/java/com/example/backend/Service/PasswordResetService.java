    public void createPasswordResetTokenForUser(User user, String token) {
        PasswordResetToken myToken = new PasswordResetToken(token, user);
        passwordTokenRepository.save(myToken);
    }

    public String validatePasswordResetToken(String token) {
        final PasswordResetToken passToken = passwordTokenRepository.findByToken(token);

        return !isTokenFound(passToken) ? "invalidToken"
                : isTokenExpired(passToken) ? "expired"
                : null;
    }

    private boolean isTokenFound(PasswordResetToken passToken) {
        return passToken != null;
    }

    private boolean isTokenExpired(PasswordResetToken passToken) {
        final Calendar cal = Calendar.getInstance();
        return passToken.getExpiryDate().before(cal.getTime());
    }


    public void changeUserPassword(User user, String password) {
        user.setPassword(encoder.encode(password));
        repo.save(user);
    }

    	public JwtResponse forgetPassword(String userEmail) {
            User user = repo.findByEmail(userEmail).get();

            if (user == null) {
             
                return new JwtResponse(null);
            }
            String token = UUID.randomUUID().toString();
            createPasswordResetTokenForUser(user, token);
            String passwordResetLink = "http://localhost:8081/Women/log/forget-password=" + token;
            String content = buildEmail(user.getUserName(), passwordResetLink);
            mailService.send(userEmail, content);
            return new JwtResponse("ok");
        }



